#include "Bar.h"

//-------------------------------------------------------------------------------------
Bar::Bar()
{
  this->max = 0.0f;
  current = max;
}

//-------------------------------------------------------------------------------------
Bar::Bar(float max)
{
  this->max = max;
  current = max;
}

//-------------------------------------------------------------------------------------
Bar::Bar(float max, float current)
{
  this->max = max;
  this->current = current;
}

//-------------------------------------------------------------------------------------
Bar::~Bar(void)
{
}

//-------------------------------------------------------------------------------------
bool Bar::isEmpty()
{
  return (current <= 0.0f);
}

//-------------------------------------------------------------------------------------
bool Bar::isFull()
{
  return (current >= max);
}

//-------------------------------------------------------------------------------------
float Bar::getFraction()
{
  return (current/max);
}

//-------------------------------------------------------------------------------------
float Bar::getCurrent()
{
  return current;
}

//-------------------------------------------------------------------------------------
float Bar::getMax()
{
  return max;
}

//-------------------------------------------------------------------------------------
void Bar::setMax(float max)
{
  this->max = max;
}

//-------------------------------------------------------------------------------------
void Bar::setCurrent(float current)
{
  this->current = current;
}

//-------------------------------------------------------------------------------------
void Bar::setEmpty()
{
  current = 0.0f;
}

//-------------------------------------------------------------------------------------
void Bar::setFull()
{
  current = max;
}


