#include "Chest.h"


Chest::Chest(void)
  : Interactive("Chest"),
    open(false),
    locked(false)
{
}

//-------------------------------------------------------------------------------------
Chest::~Chest(void)
{
}

//-------------------------------------------------------------------------------------
void Chest::setOpen(bool open)
{
  this->open = open;
}

//-------------------------------------------------------------------------------------
void Chest::setLocked(bool locked)
{
  this->locked = locked;
}

//-------------------------------------------------------------------------------------
bool Chest::isOpen()
{
  return open;
}

//-------------------------------------------------------------------------------------
bool Chest::isLocked()
{
  return locked;
}

//-------------------------------------------------------------------------------------
void Chest::interact()
{
  setOpen(!isOpen());
}